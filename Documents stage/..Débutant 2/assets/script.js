//footer
$("#footer").mouseenter(function(){
    $("#footer").css("background-color","white");
    $("#footer").css("border-top","2px solid rgb(50,50,50)");
    $(".footertext").css("color","rgb(50,50,50)");
});
$("#footer").mouseleave(function(){
    $("#footer").css("background-color","rgb(50,50,50)");
    $("#footer").css("border-top","");
    $(".footertext").css("color","white");
});

