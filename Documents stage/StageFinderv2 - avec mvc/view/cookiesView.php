<h1 class="h1_ml">Cookies utilisés sur Stage Finder</h1>

    <p class="p_ml">
        Cookies de session : ces cookies sont utilisés pour maintenir l'état de la session de 
        l'utilisateur et pour garantir que les utilisateurs ne perdent pas leur travail en cas 
        de perte de connexion ou de fermeture du navigateur.
    </p>

    <p class="p_ml">
        Cookies de préférences : ces cookies permettent à l'utilisateur de personnaliser l'apparence
        et le fonctionneement de la plateforme Stage Finder en fonction de ses préférences. Par 
        exemple, ils peuvent être utilisés pour enregistrer les choix de la langue de l'utilisateur
        ou pour activer/désactiver certaines fonctionnalités du site.
    </p>

    <p class="p_ml">
        Cookies de mesure d'audiance : ces cookies permetttent de collecter des informations sur la 
        manière dont les utilisateurs interagissent avec la plateforme Stage Finder, telles que les pages visitées le temps passé
        sur le site, les clics effeectués, etc. Ces données sont utilisées pour améliorer l'expérience 
        utilisateur et optimiser le site pour répondre aux besoins des utilisateurs.
    </p>

    <p class="p_ml">
        Cookies tiers : des cookies tiers peuvent également être utilisés sur Stage Finder
        pour des fonctions telles que la publicité ou l'analyse des données.
    </p>

    <p class="p_ml">
        Les cookies utilisés sur Stage Finder ne collectent pas d'informations personnelles 
        identifiables telles que le nom, l'adresse ou le numéro de téléphone de l'utilisateur. 
        Si vous souhaitez en savoir plus sur la façon dont nous utilisons les cookies ou si vous
        avez des questions concernant notre politique de confidentialité, veuillez contacter notre 
        équipe de support : support@stage-finder.fr.
    </p>